import time
from .color import *

def show_debug_ani(pixels):
    pixels.fill(black)
    pixels.write()

    for color in [white]:
        i = 0
        while i < pixels.n:
            pixels[i] = color
            pixels.write()
            time.sleep(0.5/pixels.n)
            i += 1

def show_debug_canvas(canvas):
    pixels = canvas.neo_pixels
    pixels.fill(black)
    pixels.write()

    for color in [red, green, blue]:
        for y in range(canvas.height):
            for x in range(canvas.width):
                canvas.set_pixel(x, y, color)
                time.sleep(0.15 / pixels.n)
                canvas.flush()

def show_debug_order(neo, order):
    neo.fill(black)
    neo.write()
    for color in [red, green, blue]:
        for i, x in enumerate(order):
            neo[x] = color
            neo.write()
        
        

